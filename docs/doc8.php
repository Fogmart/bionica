<p>
4.Доставка и приемка-передача товара
<i><a href="#Договор-оферта - Доставка ТОВАРА">см. Договор-оферта - Доставка ТОВАРА</a></i>
</p>
<p>4.1. Доставка товара, заказанного в Интернет-магазине, в согласованном количестве и ассортименте, осуществляется курьером по территории города Ставрополя, а также в другие города Российской Федерации службами доставки.&nbsp;<br /> 4.2. Расходы по доставке товара оплачиваются Покупателем по тарифам, указанным на Сайте в разделе &laquo;Оплата&raquo; и &laquo;Доставка&raquo;.&nbsp;<br /> 4.4. Отправка и доставка заказанного товара производится в оговоренные с Покупателем сроки.&nbsp;<br /> 4.5. Покупатель обязан в присутствии курьера Интернет-магазина произвести приемку заказанного товара по количеству, качеству, ассортименту и комплектности товара.&nbsp;&nbsp;<br /> 4.6. Фактом приемки товара Покупателем является оплата товара (оплата общей суммы заказа и суммы расходов по доставке товара Покупателю).</p>
